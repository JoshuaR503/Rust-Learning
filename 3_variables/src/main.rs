const MAX_POINTS: u32 = 100_000;

fn main() {

    // Variables
    let mut x = 5;

    println!("The value of x is: {}", x);

    x = 6;
    println!("The value of x is: {}", x);

    let mut name = "Joshua";

    println!("The value of name is: {}", name);
    
    name = "Josh";
    println!("The value of name is: {}", name);

    // Constants.
    println!("The maximun amount of points is: {}", MAX_POINTS);
}
